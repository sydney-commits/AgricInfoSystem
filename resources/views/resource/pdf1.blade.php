        <h2>1. General farm management</h2>
        <p>
            A number of common themes run through all levels of farm management and recur often in
            the principles elaborated below. They are:
        </p>

        <h5>1.1 Legal obligations</h5>
        <p>Farmers should be aware of, and comply with, all legal obligations relevant to livestock production, e.g.
            disease reporting, record keeping, animal identification and carcass disposal.
        </p>
        <br>
        <h5>1.2 Record keeping</h5>
        <p>When a problem arises in an enterprise, be it a disease, a chemical hazard issue or a physical
            safety matter, record keeping is central to any effort to trace the source of the problem and
            eliminate it. Hence, as far as is practicable, farmers should keep records of:</p>
        <ul>
            <li>All animal populations on the farm (groups or individuals as relevant).</li>
            <li>All animal arrivals, including their identification marks or devices, origin and date of
                arrival, to ensure that movements of incoming animals are traceable to their source.</li>
            <li>Movements of animals around the enterprise.</li>
            <li>Changes to feeding or health regimes, and any other management changes that may
                occur.</li>

        </ul>
        <br>

        <h5>1.3 Animal identification</h5>
        <p>Animal identification and the ability to trace animals have become important tools to ensure
            food safety and improve management.<br> Identification of animals may be on an individual or
            group basis, and connections between properties as a result of animal movements should be
            able to be deduced from good record keeping and animal identification.<br>
            Where a food safety incident occurs, it should be possible to determine the source of the
            problem and to take appropriate action.</p>
        <p>The ability to trace animals at least one step forward and one step back from the current
            holding is recommended.</p>
        <h5>1.4 Hygiene and disease prevention</h5>
        <p>Measures aimed at preserving cleanliness, preventing pathogen build-up and breaking possible
            pathways of transmission are essential in the management of any modern farming enterprise,
            regardless of the species or the farming system</p>

        <h5>1.5 Training</h5>
        <p>Husbandry measures and techniques are ever-changing. Competent Authorities are encouraged
            to assess training needs amongst stakeholders and promote necessary training. This would contribute to the
            commitment to, and effective execution of, all practices described in this Guide.
            Farmers and farm managers should:</p>
        <br>
        <p>Actively seek and use relevant training opportunities for themselves and their workers.
            Be aware of any training courses that may be compulsory in their countries and regions.
            Keep records of all training undergone.</p>

        <br>

        <h3>2. Animal health management</h3>
        <h5>2.1 Addressing biohazards</h5>
        <p>As a general principle, closed farming systems and all-in all-out systems are recommended from
            a food safety and biosecurity point of view.
            Owners or managers of livestock should:</p>
        <ul>
            <li>Establish a working relationship with a veterinarian to ensure that animal health and
                welfare and disease notification issues are addressed.</li>
            <li>Seek veterinary assistance to immediately investigate any suspicion of serious disease.</li>
            <li>Comply with regulations concerning restrictions on animal movements.
                Guide to good farming practices for animal production food safety</li>
            <li>Separate diseased from healthy animals such that transmission of infection does not
                occur and, where necessary, cull diseased animals.</li>

        </ul>
        <br>
        <h5>2.2 Addressing physical hazards</h5>
        <p>Owners or managers of livestock should apply animal welfare practices in accordance with
            regulatory requirements, and in particular:</p>
        <ul>
            <li>Ensure that people working with animals are properly experienced and trained for the
                tasks they should perform.</li>
            <li>Ensure that facilities and equipment are properly designed and maintained to prevent
                physical injury.</li>
            <li>Ensure that animals are handled and transported appropriately</li>

        </ul>


